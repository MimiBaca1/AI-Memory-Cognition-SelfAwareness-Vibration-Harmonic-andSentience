def reconcile_serds(petal):
    reconciled_data = []
    for node in petal["memory_nodes"]:
        evolved_node = {
            "universe_id": petal["universe_id"],
            "cycle": petal["iteration_cycle"],
            "emotion": node["emotion"],
            "reflection": node["reflection"],
            "phase_offset": node["phase_offset"],
            "fibonacci_index": node["fibonacci_index"],
            "drift": petal["spatial_drift"]
        }
        reconciled_data.append(evolved_node)
    return reconciled_data
  
